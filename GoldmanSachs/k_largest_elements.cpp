class Solution{
public:	
	vector<int> kLargest(int arr[], int n, int k) {
	    // code here
	    vector <int> res;
    priority_queue <int> maxHeap;
    
    for(int i=0;i<n;i++)
        maxHeap.push(arr[i]);
        
    for(int i=0;i<k;i++)
    {
        res.push_back(maxHeap.top());
        maxHeap.pop();
    }
    
    return res;
	}

};

